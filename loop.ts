//while loop
var a:number=1;   

while(a<= 10)
{
    console.log(a);
    a++;
}

//Do-while loop (statement will execute once after check condition)

var b:number= 1;

do{
    console.log(b);
    b++;
}
while(b<=10);

//for loop

for(var i:number=1;i<=10;i++)
{
    console.log(i);
}
